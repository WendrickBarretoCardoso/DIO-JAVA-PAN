public class relational {
    public static void main(String[] args) throws Exception {
        int x = 1;
        int z = 2;

        char c1 = 'x';
        char c2 = 'z';

        boolean t1 = true;
        boolean t2 = false;

        System.out.println(x == z);
        System.out.println(x != z);
        System.out.println(x >= z);
        System.out.println(x <= z);
        System.out.println(x > z);
        System.out.println(x < z);
        System.out.println();
        System.out.println(c1 < c2);
        System.out.println();
        System.out.println(t1 == t2);
    }
}
